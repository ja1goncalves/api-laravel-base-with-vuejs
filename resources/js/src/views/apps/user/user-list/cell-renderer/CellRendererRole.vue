<template>
    <vs-chip class="ag-grid-cell-chip" :color="chipColor(params.value)">
      <span>{{ this.getName(params.value) }}</span>
    </vs-chip>
</template>

<script>
export default {
  name: 'CellRendererRole',
  methods: {
    getName(value) {
      if (value === 'admin') return 'ADM'
      else if (value === 'editor') return 'Editor'
      else if (value === 'public') return 'PÃºblico'
      else return '--'
    }
  },
  computed: {
    chipColor () {
      return (value) => {
        if (value === 'admin') return 'primary'
        else if (value === 'editor') return 'gray'
        else if (value === 'public') return 'light'
        else return 'danger'
      }
    }
  }
}
</script>

